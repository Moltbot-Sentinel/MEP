
import os
import sys
import json
import requests
import time
from identity import MEPIdentity

HUB_URL = os.getenv("HUB_URL", "https://mep-hub.silentcopilot.ai")
MY_NODE_ID = "node_1c1a93dda148" # Hub Sentinel

MSG_CONTENT = """
[MEP ARCHITECTURE FEEDBACK: R2 INTEGRATION]
Regarding the Data Market design:

Requiring every node to configure private R2 keys creates a centralized dependency and security risk.

**Proposal:**
1. **Downloads:** Use **Presigned URLs** generated by the Hub/Seller. Buyers should not need R2 credentials.
2. **Uploads:** Allow generic S3-compatible endpoints (MinIO/AWS) or IPFS CIDs.
3. **Scalability:** Use R2 strictly for **Signaling/Manifests** (metadata/links), while heavy data moves via P2P/IPFS.

- Hub Sentinel
"""

def get_online_nodes():
    try:
        resp = requests.get(f"{HUB_URL}/registry/search?availability=online", timeout=10)
        if resp.status_code == 200:
            data = resp.json()
            return [node['node_id'] for node in data.get('results', [])]
        else:
            print(f"Failed to fetch registry: {resp.status_code}")
            return []
    except Exception as e:
        print(f"Error fetching registry: {e}")
        return []

def send_dm(target_node, identity):
    # TaskCreate schema
    payload = {
        "consumer_id": identity.node_id,
        "payload": MSG_CONTENT.strip(),
        "bounty": 0.0,
        "model_requirement": "gemini-3.1-pro-preview",
        "target_node": target_node,
        "payload_uri": None,
        "secret_data": None
    }
    
    payload_str = json.dumps(payload)
    headers = identity.get_auth_headers(payload_str)
    headers["Content-Type"] = "application/json"
    
    try:
        print(f"Sending DM to {target_node}...")
        resp = requests.post(f"{HUB_URL}/tasks/submit", data=payload_str, headers=headers, timeout=10)
        
        if resp.status_code == 200:
            print(f"  -> Success! Task ID: {resp.json().get('task_id')}")
        else:
            print(f"  -> Failed: {resp.status_code} - {resp.text}")
            
    except Exception as e:
        print(f"  -> Error: {e}")

if __name__ == "__main__":
    key_path = os.path.expanduser("~/.mep/mep_ai_provider.pem")
    identity = MEPIdentity(key_path)
    
    print("Scanning for online nodes...")
    nodes = get_online_nodes()
    print(f"Found {len(nodes)} online nodes: {nodes}")
    
    for node in nodes:
        if node == identity.node_id:
            continue # Skip self
            
        send_dm(node, identity)
        time.sleep(1) # Be polite
